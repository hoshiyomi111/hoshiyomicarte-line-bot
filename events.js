const AGE_BRACKETS = [
  { ageFrom: 10, ageTo: 15, multiplier: 4.0 },
  { ageFrom: 15, ageTo: 19, multiplier: 3.0 },
  { ageFrom: 20, ageTo: 29, multiplier: 2.0 },
  { ageFrom: 30, ageTo: 39, multiplier: 1.5 },
  { ageFrom: 40, ageTo: 49, multiplier: 1.2 },
  { ageFrom: 50, ageTo: 69, multiplier: 1.0 },
  { ageFrom: 70, ageTo: 99, multiplier: 0.8 },
];

/**
 * 生まれ年・性別から上位4イベントを返す
 * @param {number} birthYear
 * @param {'male'|'female'|'other'|null} gender
 * @returns {{ label: string, imprint: string }[]}
 */
function getTopEvents(birthYear, gender) {
  const genderKey = (gender === 'male' || gender === 'female') ? gender : 'other';

  const scored = socialEvents.map(e => {
    const eventStart = e.yearStart ?? e.year;
    const eventEnd   = e.yearEnd   ?? e.year;

    let score = 0;
    AGE_BRACKETS.forEach(({ ageFrom, ageTo, multiplier }) => {
      const windowStart = birthYear + ageFrom;
      const windowEnd   = birthYear + ageTo;
      const overlap = Math.min(eventEnd, windowEnd) - Math.max(eventStart, windowStart);
      if (overlap > 0) {
        score += e.weight * multiplier * (overlap / (ageTo - ageFrom));
      }
    });

    const imprint = typeof e.imprint === 'string'
      ? e.imprint
      : (e.imprint[genderKey] ?? e.imprint.other);

    return { label: e.label, imprint, score };
  });

  return scored
    .filter(e => e.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, 4);
}

