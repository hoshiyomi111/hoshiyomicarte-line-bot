function getZodiac(birthdate) {
  const match = birthdate.match(/(?:\d{2,4}[\/\-年])?(\d{1,2})[\/\-月](\d{1,2})/);
  if (!match) return null;
  const month = parseInt(match[1]);
  const day = parseInt(match[2]);

  if ((month === 3 && day >= 21) || (month === 4 && day <= 19)) return '牡羊座';
  if ((month === 4 && day >= 20) || (month === 5 && day <= 20)) return '牡牛座';
  if ((month === 5 && day >= 21) || (month === 6 && day <= 21)) return '双子座';
  if ((month === 6 && day >= 22) || (month === 7 && day <= 22)) return '蟹座';
  if ((month === 7 && day >= 23) || (month === 8 && day <= 22)) return '獅子座';
  if ((month === 8 && day >= 23) || (month === 9 && day <= 22)) return '乙女座';
  if ((month === 9 && day >= 23) || (month === 10 && day <= 23)) return '天秤座';
  if ((month === 10 && day >= 24) || (month === 11 && day <= 22)) return '蠍座';
  if ((month === 11 && day >= 23) || (month === 12 && day <= 21)) return '射手座';
  if ((month === 12 && day >= 22) || (month === 1 && day <= 19)) return '山羊座';
  if ((month === 1 && day >= 20) || (month === 2 && day <= 18)) return '水瓶座';
  if ((month === 2 && day >= 19) || (month === 3 && day <= 20)) return '魚座';
  return null;
}

// ─────────────────────────────────────────────
// メッセージ定義
// ─────────────────────────────────────────────
const MSG = {
  welcome: `はじめまして🌙 星読みカルテへようこそ。

占星術を通して、あなたの人間関係のパターンを一緒に読み解きます。
断定や予言ではなく、「腑に落ちるかどうか」を大切にするサービスです。

まず、あなたのお名前を教えてください✨
（ニックネームでもOKです）`,

  askBirthdate: (name) =>
    `${name}さん、よろしくお願いします🌟\n\n生年月日を教えてください。\n例：1995/8/15`,

  askBirthtime: (zodiac) =>
    `${zodiac}ですね✨\n\n出生時間帯を教えてください。\nわからない場合は「スキップ」でも大丈夫です。`,

  askBirthplace: `出生地（都道府県や市区町村）を教えてください。\nわからない場合は「スキップ」と入力してください。`,

  confirm: (s) =>
    `以下の内容で診断します📋\n\n名前：${s.name}\n生年月日：${s.birthdate}\n星座：${s.zodiac}\n出生時間帯：${s.birthtime || '未入力'}\n出生地：${s.birthplace || '未入力'}\n\n「送信」と入力すると診断結果をお届けします🔮`,

  done: (name) =>
    `${name}さんの星読みカルテを準備中です🌙\nしばらくお待ちください✨`,
};

// ─────────────────────────────────────────────
// セッション管理
// ─────────────────────────────────────────────
const sessions = {};

function getSession(userId) {
  if (!sessions[userId]) {
    sessions[userId] = { step: 0 };
  }
  return sessions[userId];
}

// ─────────────────────────────────────────────
// LINEメッセージ送信ヘルパー
// ─────────────────────────────────────────────
async function reply(token, text) {
  await client.replyMessage(token, { type: 'text', text });
}

async function push(userId, text) {
  await client.pushMessage(userId, { type: 'text', text });
}

// ─────────────────────────────────────────────
// メインハンドラ
// ─────────────────────────────────────────────
async function handleMessage(event) {
  if (event.type !== 'message' || event.message.type !== 'text') return;

  const userId = event.source.userId;
  const text = event.message.text.trim();
  const token = event.replyToken;
  const session = getSession(userId);

  // リセットコマンド
  if (text === 'リセット' || text === 'reset') {
    sessions[userId] = { step: 0 };
    await reply(token, MSG.welcome);
    return;
  }

  switch (session.step) {
    case 0: {
      // 診断 or 友だち追加 → ウェルカム
      if (text.includes('診断') || text === 'start' || text === 'はじめる') {
        await reply(token, MSG.welcome);
        session.step = 1;
      } else {
        await reply(token, MSG.welcome);
        session.step = 1;
      }
      break;
    }

    case 1: {
      // 名前
      session.name = text;
      await reply(token, MSG.askBirthdate(session.name));
      session.step = 2;
      break;
    }

    case 2: {
      // 生年月日
      session.birthdate = text;
      const zodiac = getZodiac(text);
      if (!zodiac) {
        await reply(token, '生年月日の形式を確認できませんでした🙏\n例：1995/8/15 の形式で入力してください。');
        break;
      }
      session.zodiac = zodiac;

      // クイックリプライで時間帯を選択
      await client.replyMessage(token, {
        type: 'text',
        text: MSG.askBirthtime(zodiac),
        quickReply: {
          items: [
            { type: 'action', action: { type: 'message', label: '🌅 朝（0〜11時）', text: '朝' } },
            { type: 'action', action: { type: 'message', label: '☀️ 昼（12〜17時）', text: '昼' } },
            { type: 'action', action: { type: 'message', label: '🌙 夜（18〜23時）', text: '夜' } },
            { type: 'action', action: { type: 'message', label: 'わからない', text: 'わからない' } },
            { type: 'action', action: { type: 'message', label: 'スキップ', text: 'スキップ' } },
          ],
        },
      });
      session.step = 3;
      break;
    }

    case 3: {
      // 出生時間帯
      session.birthtime = text === 'スキップ' ? null : text;
      await reply(token, MSG.askBirthplace);
      session.step = 4;
      break;
    }

    case 4: {
      // 出生地
      session.birthplace = text === 'スキップ' ? null : text;
      await reply(token, MSG.confirm(session));
      session.step = 5;
      break;
    }

    case 5: {
      // 確認 → 送信
      if (text === '送信' || text === 'はい' || text === 'ok' || text === 'OK') {
        await reply(token, MSG.done(session.name));

        // 返却文を自動送信
        const reading = ZODIAC_READINGS[session.zodiac];
        const readingMsg = reading
          ? `${session.name}さんへ、星読みカルテをお届けします🌙\n\n${reading}`
          : `${session.name}さんの星座（${session.zodiac}）の解析を準備中です。\n少々お待ちください✨`;

        await push(userId, readingMsg);

        // 管理者通知
        const adminId = process.env.ADMIN_LINE_USER_ID;
        if (adminId) {
          const adminMsg = [
            '📋 新しい診断が完了しました',
            `名前：${session.name}`,
            `生年月日：${session.birthdate}`,
            `星座：${session.zodiac}`,
            `出生時間帯：${session.birthtime || '未入力'}`,
            `出生地：${session.birthplace || '未入力'}`,
          ].join('\n');
          await push(adminId, adminMsg);
        }

        // セッションリセット
        sessions[userId] = { step: 0 };
      } else {
        await reply(token, '「送信」と入力すると診断結果をお届けします🔮\n最初からやり直す場合は「リセット」と入力してください。');
      }
      break;
    }

    default:
      sessions[userId] = { step: 0 };
      await reply(token, MSG.welcome);
  }
}

// ─────────────────────────────────────────────
// 友だち追加イベント
// ─────────────────────────────────────────────
async function handleFollow(event) {
  const userId = event.source.userId;
  sessions[userId] = { step: 1 };
  await client.replyMessage(event.replyToken, { type: 'text', text: MSG.welcome });
}

// ─────────────────────────────────────────────
// Express ルーティング
// ─────────────────────────────────────────────
app.post('/webhook', line.middleware(config), (req, res) => {
  Promise.all(
    req.body.events.map((event) => {
      if (event.type === 'message') return handleMessage(event);
      if (event.type === 'follow') return handleFollow(event);
      return Promise.resolve();
    })
  )
    .then(() => res.status(200).json({ status: 'ok' }))
    .catch((err) => {
      console.error('Webhook error:', err);
      res.status(500).json({ error: err.message });
    });
});

app.get('/', (req, res) => res.send('星読みカルテ LINE Bot is running 🌙'));

const PORT = process.env.PORT || 3000;
app.listen(PORT, () => console.log(`Server running on port ${PORT}`));
